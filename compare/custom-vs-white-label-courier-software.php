                    <p><strong>White-label wins</strong> when you need to be in market in 4-8 weeks, your workflow is broadly standard, you're under ~25,000 shipments/month, and the platform isn't your differentiator &mdash; just a way to operate.</p>
                    <p><strong>Custom wins</strong> when the platform <em>is</em> your differentiator, your workflow has real quirks, you're past 25,000-30,000 shipments/month, or you want code, schema and roadmap ownership for fund-raising or M&amp;A reasons.</p>
