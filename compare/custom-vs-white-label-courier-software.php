                            <ul>
                                <li>You need to be <strong>operational in 4-8 weeks</strong>, not 4-7 months.</li>
                                <li>Your workflow is broadly standard: pickup &rarr; hub &rarr; manifest &rarr; delivery &rarr; billing.</li>
                                <li>You're doing <strong>under ~25,000 shipments/month</strong>; per-shipment licensing is still comfortable.</li>
                                <li>You don't have an in-house product/tech team and don't want one.</li>
                                <li>You're <strong>validating a new business</strong> &mdash; learning the workflow before committing to a build.</li>
                                <li>The platform is a way to operate, not the thing customers buy from you.</li>
                                <li>You're OK with vendor-controlled roadmap and configuration limits.</li>
                            </ul>
                        </div>
                        <div class="cmp-pick-card win-custom">
                            <h3><i class="fas fa-check-circle" style="color:var(--cs-primary);margin-right:6px;"></i>Pick a custom build if</h3>
