                <div class="cmp-block">
                    <h2>What "white-label" really means in courier software</h2>
                    <p>White-label courier software is a pre-built logistics platform that a vendor licenses to you under your brand. You get the core modules &mdash; booking, pickup, hub in/out scan, manifest, tracking, billing &mdash; out of the box. The UI is re-skinnable: your logo, your colours, your domain, often your customer-facing tracking page.</p>
                    <p>What you do <em>not</em> get: the source code, the database schema, control over the roadmap, or the freedom to ship a custom workflow rule the vendor hasn't built. White-label is a rental of capability, not a transfer of ownership.</p>
                    <p>This is meaningfully different from a SaaS like Shipsy or LogiNext (which is multi-tenant and brand-visible) and meaningfully different from a custom build (where the codebase is yours). White-label sits in the middle: branded as yours, owned by the vendor.</p>
                </div>

                <div class="cmp-block">
                    <h2>Who we are &mdash; and why we're not neutral</h2>
                    <p>ITD GrowthLabs builds <a href="../products/courier-management-software.php">custom courier and logistics software</a>. So if a comparison favours custom, that's our commercial bias on the page. We've also white-labelled our own products to a handful of operators where a 5-month custom build genuinely didn't make sense for them &mdash; so this isn't a one-track sales page.</p>
                    <p>The honest truth: most operators don't need the build path. White-label is the right answer for the majority. It becomes the wrong answer at a specific volume and differentiation threshold &mdash; which is what the rest of this page maps out.</p>
                </div>

