                    <h2>FAQ</h2>
                    <div class="cmp-faq">
                        <details>
                            <summary>What is white-label courier software?</summary>
                            <p>A pre-built logistics platform a vendor licenses to you under your brand. You get core booking, hub, manifest, tracking and billing modules out of the box, re-skinnable with your logo and domain. It ships in 4-8 weeks but the underlying code, database schema and roadmap stay vendor-controlled.</p>
                        </details>
                        <details>
                            <summary>Is white-label cheaper than building custom courier software?</summary>
                            <p>In year one, almost always yes. White-label setup typically lands in the Rs 6-15 lakh range plus monthly licensing. A custom build runs Rs 35-70 lakhs upfront. Over 5 years, custom often wins on absolute cost above ~30,000 shipments per month, because licensing scales with volume but custom hosting stays roughly flat.</p>
                        </details>
                        <details>
                            <summary>Can I start with white-label and migrate to custom later?</summary>
                            <p>Yes, and this is a sensible path for many operators. White-label gets you live in 4-8 weeks while you validate the business and accumulate workflow data. Once volumes pass 25,000-30,000 shipments/month and licensing starts compounding, a custom rebuild typically takes 5-7 months with full data migration. Plan one quarter of dual-run.</p>
                        </details>
                        <details>
                            <summary>What can't I customise on a white-label platform?</summary>
                            <p>Branding (logo, colours, domain) is almost always configurable. What is not: core workflow logic, pricing engine internals, database schema, API contract, or integrations the vendor hasn't built. If your operation has unusual hub flows, weight-dispute logic, or zone pricing rules, those go on the vendor's roadmap rather than into your sprint.</p>
                        </details>
                        <details>
                            <summary>Who owns the data on a white-label platform?</summary>
                            <p>You own the operational data (shipments, customers, invoices) but the vendor owns the platform configuration, integrations and database schema. Most white-label vendors give you CSV/API exports of historical data, but workflow rules, pricing logic and carrier-level configuration usually have to be rebuilt if you migrate to custom.</p>
                        </details>
                        <details>
                            <summary>How long does white-label vs custom take to launch?</summary>
                            <p>White-label: 4-8 weeks for branding, integration with 3-5 carriers, basic workflow configuration and pilot launch. Custom: 4-7 months for a production-ready v1 covering booking, hub-scan, manifest, tracking and billing, with another 2-3 months for multi-carrier rate aggregation and COD remittance. The gap is biggest on day one and shrinks over the next 2 years.</p>
                        </details>
