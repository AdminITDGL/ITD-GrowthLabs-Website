                        <li><strong>Weeks 13-16:</strong> pilot cutover on one hub or one corporate client. New platform owns bookings; SaaS kept warm.</li>
                        <li><strong>Weeks 17-20:</strong> rolling migration to remaining hubs. Dual-run manifests for 1 week each.</li>
                        <li><strong>Weeks 21-22:</strong> billing and finance reconciliation switch. SaaS exports final history.</li>
                        <li><strong>Week 23:</strong> SaaS contract sunset / non-renewal.</li>
