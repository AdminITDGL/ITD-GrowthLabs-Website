                    <h2>FAQ</h2>
                    <div class="cmp-faq">
                        <details>
                            <summary>Is a custom courier software really cheaper than Shipsy or LogiNext?</summary>
                            <p>It depends on volume and timeline. A typical custom courier platform costs Rs 35-70 lakhs to build in year one versus roughly Rs 12-25 lakhs/year in SaaS licensing + per-shipment fees on mid-volume plans. Over 3 years, custom usually breaks even between year 2 and 3 for operators doing 50,000+ shipments a month. Below that volume, SaaS typically wins on absolute cost.</p>
                        </details>
                        <details>
                            <summary>How long does it take to build a custom courier platform?</summary>
                            <p>For a core platform covering booking, pickup, hub scan, manifest, tracking and billing: 4-7 months to a usable v1 with a 6-10 engineer team. Multi-carrier rate aggregation, COD remittance, and NDR workflows usually take another 2-3 months. Shipsy or LogiNext can get a pilot live in 2-4 weeks.</p>
                        </details>
                        <details>
                            <summary>Can I migrate from Shipsy or LogiNext without disrupting operations?</summary>
                            <p>Yes, with a dual-run rollout. Most operators run the new platform in shadow mode for 2-3 weeks, switch bookings first, then hub/manifest, then billing last. A single-hub or single-corporate-client pilot typically de-risks the rollout before a full switch.</p>
                        </details>
                        <details>
                            <summary>What happens to my carrier integrations if I switch?</summary>
                            <p>Carrier APIs are standardised (rates, book, label, track, cancel) and most Indian carriers publish public-ish integration docs. Building 5-10 carriers takes about 8-12 engineering weeks. With a custom build you own the adapters; you can add/remove/re-negotiate carriers without vendor permission.</p>
                        </details>
                        <details>
                            <summary>Is Shipsy or LogiNext better?</summary>
                            <p>Both are credible SaaS platforms with overlapping feature sets. Shipsy skews more toward 3PL and multi-carrier aggregation; LogiNext has deeper on-road transportation and last-mile visibility tooling. Pick based on your actual use case, not brand &mdash; most serious buyers run both as short pilots before deciding.</p>
                        </details>
                        <details>
                            <summary>Do I lose my data if I leave a SaaS platform?</summary>
                            <p>Most SaaS platforms let you export historical shipments, customers, and invoices via API or CSV. You typically do not get the full operational configuration (routing rules, pricing engine state, carrier-level contract data) in a machine-readable form &mdash; that has to be rebuilt. Plan a 4-8 week data migration window when switching to a custom build.</p>
                        </details>
