                    <h2>The client &amp; the problem</h2>
                    <p>The brand had crossed Rs 12 Cr ARR on Shopify but mobile web conversion was stuck at 1.6% and CAC kept rising as they scaled spend. They had ruled out Shopify's own mobile-app builders (too rigid, no offline cart, slow Storefront API performance) and wanted a real native app — but didn't want to fork their backend or move off Shopify.</p>
                    <ul>
                        <li><strong>Mobile-web conversion ceiling:</strong> 1.6% on mobile web; everything they did to push it went sideways. Pushy banners hurt brand. Trimming steps hurt accuracy.</li>
                        <li><strong>Repeat-rate floor:</strong> 22% repeat in 60 days. Email was a noisy channel; WhatsApp Business was unmanaged.</li>
                        <li><strong>Re-engagement gap:</strong> No push notifications, no abandoned-cart that actually worked, no tier/loyalty surface.</li>
                        <li><strong>Performance budget:</strong> Founders refused to compromise on the brand experience. Time-to-product-image had to be under 1 second on 4G.</li>
                        <li><strong>Shopify lock-in tradeoff:</strong> Migrating off Shopify was off the table. The solution had to live alongside Shopify, not replace it.</li>
                    </ul>
                </div>

                <div class="cs-block">
                    <h2>The solution</h2>
                    <p>Flutter app + Shopify Storefront API + a thin loyalty/CRM layer of our own. The architecture: read-mostly catalog from Shopify (with aggressive caching), write-through cart + checkout via Shopify Checkout API, and a separate Postgres-backed service for everything Shopify doesn't do well — push, WhatsApp, loyalty, custom merchandising.</p>
                    <h3>1. Customer app (Flutter)</h3>
                    <p>Brand-first home (editorial-style), category browse, product detail with zoom + 360° + size-guide, cart with offline support, Shopify Checkout in WebView (PCI-safe), order history, wishlist, loyalty tier display, profile.</p>
                    <h3>2. Catalog cache layer</h3>
                    <p>Shopify Storefront API is rate-limited and slow at 200 RPS. We cache catalog reads in Redis with a 90-second TTL, invalidated by Shopify webhooks. Time-to-first-product-image dropped from 3.4s to 0.8s on 4G.</p>
                    <h3>3. CRM / re-engagement service</h3>
                    <p>Postgres + Node.js + a small queue. Owns: push tokens, customer cohort tags, abandoned-cart triggers, WhatsApp templates, Klaviyo sync, loyalty point engine. Sits beside Shopify; talks to it via webhooks + Storefront API.</p>
                    <h3>4. Admin web (React)</h3>
                    <p>Marketer-facing: design app banners, push-campaign builder with cohort targeting, WhatsApp template manager (Meta-approved), loyalty rules, A/B-test config, behaviour analytics. The brand team self-serves; we don't ship code for every campaign.</p>
                    <h3>5. Analytics &amp; observability</h3>
                    <p>Mixpanel for behaviour, Sentry for errors, Datadog for infra, GA4 for top-of-funnel attribution. We attribute install-to-purchase and surface a CAC-by-channel report the founders look at every Monday.</p>
                </div>

