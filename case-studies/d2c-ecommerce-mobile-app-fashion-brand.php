                    <p><strong>Why not Shopify Mobile App Builder:</strong> Shoptrade et al hit the same Storefront-API performance wall and the templated UX would not have cleared the brand's bar. Building it ourselves cost 1.4× theirs and shipped a product the brand could iterate on for years.</p>
                    <p><strong>Why Flutter:</strong> single codebase, brand-aligned design system, and the 60fps animation budget that the brand team wanted for product-detail interactions. iOS App Store approval was three weeks, Play Store two days.</p>
                    <p><strong>Why a separate CRM layer:</strong> Shopify Flow is fine for simple cart-abandon emails. It cannot do cohort + behaviour-targeted push, loyalty math, or WhatsApp-first journeys. Building a thin CRM service was 6 weeks of work and unlocked four channels.</p>
                </div>

                <div class="cs-block">
                    <h2>The hardest technical problem we solved</h2>
                    <p><strong>WhatsApp-first re-engagement that converts.</strong></p>
<p>Email is dying. Push is shrinking. WhatsApp open rates are 90%+. Building a WhatsApp re-engagement loop that does not get the BSP account banned was a 6-week project on its own.</p><p>The constraints: only Meta-approved template categories (utility / authentication / marketing) can be sent outside the 24-hour customer-care window. Marketing templates need explicit opt-in. Frequency caps are unforgiving. Get any of this wrong and Meta restricts the WABA.</p><p>We modelled WhatsApp like a strict SMTP — every send is template-typed, frequency-capped per cohort, and logged for opt-out. Open rates hit 91%. Click rates 18%. Conversion-from-WhatsApp is 5x email and 2x push.</p><p>This single channel moved repeat-rate from 28% (post-app launch) to 41%.</p>
                </div>

                <div class="cs-block">
                    <h2>Go-to-market &amp; ramp</h2>
                    <p>App-launch campaign was a one-week WhatsApp + email blast to existing customers, gating exclusive access to a new collection behind the app install. 14,200 installs in week one with zero ad spend.</p><p>Paid acquisition kicked in at month two. Apple Search Ads and Meta App Install ads outperformed mobile-web Meta ads on 60-day LTV by 2.3×. By month four the app was the brand's highest-margin acquisition channel.</p>
