                <div class="cs-block">
                    <h2>The client &amp; the problem</h2>
                    <p>Cross-border is harder than domestic by an order of magnitude. Three currencies means three rate cards. Six carriers means six API surfaces. Eight customs document formats means a wrong field on any one means the parcel sits at customs for three weeks. The client was running this on Excel and email and was ready to fold the international lane.</p>
                    <ul>
                        <li><strong>Multi-currency billing chaos:</strong> Quotes were given in INR but invoices generated in AED or GBP using yesterday's exchange rate. Customers disputed 9% of invoices.</li>
                        <li><strong>Carrier API fragmentation:</strong> FedEx, Aramex, DHL, IndiaPost-Speedpost, Royal Mail, Shyplite — six APIs, six auth schemes, six tracking formats, six SLA-clock conventions.</li>
                        <li><strong>Customs documents:</strong> Commercial invoice, packing list, AWB, certificate of origin, IEC declaration, BOE, CN22/CN23 — different combinations per lane, all manually filled.</li>
                        <li><strong>Lane-specific SLAs:</strong> India-UAE 4-day SLA, India-UK 7-day SLA, UAE-UK 5-day SLA — but partner-carrier SLAs varied and were tracked nowhere.</li>
                        <li><strong>Tracking blackouts:</strong> Once a parcel left India and was handed off to a destination-country carrier, tracking went dark for 1-3 days.</li>
                    </ul>
                </div>

                <div class="cs-block">
                    <h2>The solution</h2>
                    <p>A platform with three layers: a customer-facing booking portal, an ops admin, and a carrier-integration layer that abstracts the six carriers behind a single internal API. The non-obvious decision was building the customs document engine as a configurable rules engine rather than per-lane templates.</p>
                    <h3>1. Customer portal (Next.js)</h3>
                    <p>Quote → book → pay → track → invoice. Quotes in customer's currency. Documents auto-generated from the booking form. Tracking aggregated from all carriers in their journey. Embedded support chat with WhatsApp + email continuity.</p>
                    <h3>2. Ops admin (React)</h3>
                    <p>Single shipment board across all lanes and carriers. SLA-clock per leg. Customs-doc validator (catches 'no IEC for export' before the parcel leaves the warehouse). Reconciliation against carrier invoices. Multi-currency P&amp;L reports.</p>
                    <h3>3. Carrier-integration layer (Node.js)</h3>
                    <p>Six adapters, one internal interface. Each adapter handles auth (OAuth / token / API key), rate-pull, booking, label generation, tracking, and proof-of-delivery in the carrier's native format and translates to our canonical model.</p>
                    <h3>4. Customs-doc engine</h3>
                    <p>JSON-driven rules engine: per-lane (India→UK, India→UAE, UAE→UK, etc) it defines required documents, fields, validations, and currency. Generating commercial invoice + packing list + CN22 for an India-UK shipment is a single API call.</p>
                    <h3>5. Backend (Node.js + PostgreSQL)</h3>
                    <p>Postgres for source of truth (multi-currency money columns with currency code, never a float for cash), Redis for FX-rate cache (refreshed every 4 hours from a paid feed), Bull queue for carrier API calls and webhook ingestion.</p>
                </div>

                <div class="cs-block">
                    <h2>Tech stack &amp; why</h2>
                    <p>We picked the stack for fit, performance under realistic load, and operational simplicity. Here is the breakdown:</p>
                    <div style="margin-bottom: 16px;">
                        <span class="tech-chip">Node.js (NestJS)</span>
                        <span class="tech-chip">TypeScript</span>
                        <span class="tech-chip">Next.js</span>
                        <span class="tech-chip">React</span>
                        <span class="tech-chip">PostgreSQL</span>
                        <span class="tech-chip">Redis</span>
                        <span class="tech-chip">Bull</span>
                        <span class="tech-chip">Stripe (multi-currency)</span>
                        <span class="tech-chip">Razorpay</span>
                        <span class="tech-chip">AWS</span>
                        <span class="tech-chip">Datadog</span>
                        <span class="tech-chip">Sentry</span>
                        <span class="tech-chip">GitHub Actions</span>
                    </div>
                    <p><strong>Multi-currency money is non-trivial:</strong> we stored every monetary value as <code>(amount_minor, currency_code)</code> tuples — never as floats, never as a single 'amount' column. FX is applied at booking, never at invoice. The 9% customer dispute rate dropped to under 0.5% in two months because invoices matched quotes exactly.</p>
                    <p><strong>Carrier adapter pattern:</strong> behind the internal API, each carrier has its own adapter class with the same six methods. Adding a seventh carrier (Singapore Post, planned) is a 5-day project, not a 5-month one.</p>
                    <p><strong>Stripe for AED + GBP, Razorpay for INR:</strong> regulatory reality. Stripe doesn't support INR receiver-side; Razorpay doesn't support AED + GBP. Two PSPs, one unified payment-status surface in our app.</p>
                </div>

                <div class="cs-block">
                    <h2>The hardest technical problem we solved</h2>
                    <p><strong>Customs paperwork that doesn't get the parcel stuck.</strong></p>
<p>The hardest engineering problem on the platform. Customs paperwork is harsh: get the HSN code wrong on a commercial invoice and the parcel sits at customs for 3-21 days. Get the IEC missing and India customs rejects the export entirely. Get the CN22 weight wrong and Royal Mail returns the parcel to UK origin.</p><p>We talked to three customs brokers across the lanes, encoded their rules into a rules engine, and validated every booking against it before it could leave the originating warehouse.</p><p>Sample rules: India→UK air parcel under £270 declared value needs CN22 with HSN code; over £270 needs CN23; courier over UK£135 needs UK VAT collected at origin; India-export needs IEC + GSTIN + AD code. Each rule is data, not code.</p><p>Customs holds dropped from 11% (eyeballing the prior month) to under 0.4% in three months. That alone returned the platform's build cost.</p>
                </div>

                <div class="cs-block">
                    <h2>Go-to-market &amp; ramp</h2>
                    <p>We launched on the India-UAE lane only — single carrier (Aramex), single currency for invoicing initially (INR with FX-locked AED display). Got the platform proven on one lane before adding complexity.</p><p>Lane-by-lane rollout: India-UK in month two, UAE-UK in month four, multi-carrier per-lane in month six. Singapore lane is in design now. Each lane required its own customs SME conversation; that's the rate-limiter, not engineering.</p>
                </div>

