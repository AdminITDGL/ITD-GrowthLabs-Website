                <div class="cs-block">
                    <h2>The client &amp; the problem</h2>
                    <p>The client runs a last-mile courier business that started with a handful of riders in a single city. Growth was capped by operations: a dispatcher manually matched incoming WhatsApp and phone requests with available riders, routes were decided by guesswork, and billing was reconciled from paper slips once a week. Each new city added a dispatcher, a new WhatsApp group, and another layer of spreadsheet reconciliation.</p>
                    <p>By the time they came to us, the founders knew exactly what was wrong but did not have an internal tech team to fix it. They needed a technology partner who could take the idea from whiteboard to production — not just a code shop. Their specific pain points:</p>
                    <ul>
                        <li><strong>No self-serve booking:</strong> customers had to chat or call, which capped volume to the dispatcher's bandwidth.</li>
                        <li><strong>No driver visibility:</strong> riders had no live view of the job queue, earnings, or navigation.</li>
                        <li><strong>No tracking for customers:</strong> every "where is my parcel?" query was handled manually.</li>
                        <li><strong>Billing chaos:</strong> invoicing was a weekly, error-prone batch process that delayed payouts.</li>
                        <li><strong>No data:</strong> no dashboard for order volume, partner utilisation, dispute rate, or unit economics.</li>
                    </ul>
                </div>

                <div class="cs-block">
                    <h2>The solution: a three-app platform with shared infrastructure</h2>
                    <p>We scoped the platform as three tightly-integrated products plus a shared backend. This split is standard for on-demand logistics, but the devil is in the details — especially around real-time state sync and failure handling.</p>

                    <h3>1. Customer app (Flutter, iOS + Android)</h3>
                    <p>Clean booking flow: source address (with Google Places autocomplete), destination, parcel type/weight, pickup slot, delivery slot, payment. Live tracking once assigned, ratings on completion, saved addresses, booking history, and re-book. Push notifications at each status change (assigned, picked up, in transit, delivered).</p>

                    <h3>2. Driver (courier partner) app (Flutter)</h3>
                    <p>Rider logs in, goes online, receives job offers based on proximity and current load. Accepts → gets turn-by-turn navigation, pickup/delivery OTP verification, in-app earnings wallet, daily and weekly reports, support chat. Offline-tolerant: the app queues state changes when the network drops and syncs on reconnect.</p>

                    <h3>3. Admin dashboard (React web app)</h3>
                    <p>Operations' control tower. Live map of all active jobs and riders, manual override for tricky assignments, dispute resolution queue, rider onboarding with KYC, zone/pricing configuration, payout approval, MIS reports, and configurable alert rules (e.g. "pager me if any job is un-assigned for 90 seconds").</p>

                    <h3>4. Backend (Node.js + Firebase)</h3>
                    <p>REST API for CRUD, Firebase Realtime Database for live job and location streams, Cloud Functions for event-driven workflows (SMS, payment capture, invoicing, pay-out triggers). Razorpay for payments. Google Maps Platform for geocoding, directions, and ETA. All services behind an API gateway with rate-limiting and observability (Datadog).</p>
                </div>

                <div class="cs-block">
                    <h2>Tech stack &amp; why</h2>
                    <p>We picked the stack for three reasons: <strong>shared code between apps</strong>, <strong>real-time without building it ourselves</strong>, and <strong>cheap to operate at scale</strong>. Here is the breakdown:</p>
                    <div style="margin-bottom: 16px;">
                        <span class="tech-chip">Flutter 3.x</span>
                        <span class="tech-chip">Dart</span>
                        <span class="tech-chip">React 18</span>
                        <span class="tech-chip">Node.js (Express)</span>
                        <span class="tech-chip">TypeScript</span>
                        <span class="tech-chip">Firebase RTDB</span>
                        <span class="tech-chip">Firestore</span>
                        <span class="tech-chip">Firebase Cloud Messaging</span>
                        <span class="tech-chip">Google Maps Platform</span>
                        <span class="tech-chip">Razorpay</span>
                        <span class="tech-chip">AWS S3</span>
                        <span class="tech-chip">Datadog</span>
                        <span class="tech-chip">GitHub Actions CI/CD</span>
                    </div>
                    <p><strong>Flutter for both apps:</strong> one codebase, two stores. Shaved ~35% off what a native iOS + native Android build would have cost. Performance on mid-range Android — the most common rider device — was indistinguishable from native in benchmarks.</p>
                    <p><strong>Firebase for real-time:</strong> building reliable presence and location streaming from scratch is a multi-month project in itself. Firebase gives us that off the shelf, with battle-tested offline sync. Cost remained predictable because we kept hot collections small and moved cold data to Firestore.</p>
                    <p><strong>Node.js on the backend:</strong> the team's dominant language, plus easy interop with Firebase Functions. Typed end to end with TypeScript for schema safety.</p>
                </div>

                <div class="cs-block">
                    <h2>The hardest technical problem we solved</h2>
                    <p>Job assignment. Everything else is table stakes. Assignment is what makes or breaks an on-demand platform.</p>
                    <p>The naive approach — "assign the nearest available rider" — fails in three ways: it overloads hotspot zones, it ignores rider load (a driver with 3 pending jobs is not "available"), and it doesn't handle declined offers gracefully. Our scoring function combines:</p>
                    <ul>
                        <li><strong>Distance to pickup</strong> (Haversine, then refined with actual road ETA from Maps).</li>
                        <li><strong>Rider load</strong> (current active jobs, weighted by estimated completion time).</li>
                        <li><strong>Rider rating</strong> (last 30 days) — a small bonus to protect CSAT.</li>
                        <li><strong>Decline streak</strong> — a cooldown so repeat decliners get fewer offers.</li>
                        <li><strong>Zone parity</strong> — small bonus for riders who help balance an underserved zone.</li>
                    </ul>
                    <p>Offers are broadcast to the top 3 candidates simultaneously with a 12-second timer. First acceptance wins; the others are told the job is gone. If all 3 decline or time out, we expand the radius and retry. This single change cut unassigned jobs from 6% to under 1.5%.</p>
                </div>

                <div class="cs-block">
                    <h2>Go-to-market &amp; ramp</h2>
                    <p>The v1 launch was intentionally narrow: one city, two zones, 20 hand-picked riders, and a 90-day soft-launch with heavy ops involvement. This gave us a tight feedback loop to fix the embarrassing bugs (there were plenty — mostly around edge cases in payment capture and OTP timing) before scaling.</p>
                    <p>City expansion after month 3 was driven by a configuration-first architecture: new cities mean adding zones, pricing, and cut-off times in a database, not shipping code. The fourth city onboarded in four working days end to end.</p>
                </div>

