                <div class="cs-block">
                    <h2>The client &amp; the problem</h2>
                    <p>The client is a two-decade-old courier network operating across western India with 14 hubs, a 60-vehicle line-haul fleet, and a 400-strong corporate client list. Business was healthy. The back-office was not.</p>
                    <p>Every pickup was written in a booking book, transcribed to Excel at end of shift, manifested by reading out AWBs on a WhatsApp group, billed from a separate Excel sheet once a month, and tracked by calling the hub manager on his personal phone. The founder's son, who had taken over operations, knew the model wouldn't survive another doubling of volume. His specific pain points:</p>
                    <ul>
                        <li><strong>Data entered 3–4 times:</strong> once at pickup, once at hub-in, once at hub-out, once at billing. Each transcription introduced errors.</li>
                        <li><strong>Billing errors costing money:</strong> ~8 billing disputes per month, most traced to AWB/weight/service mismatches between Excel sheets.</li>
                        <li><strong>No visibility between hubs:</strong> a shipment "lost" in transit often just meant nobody had updated the WhatsApp group.</li>
                        <li><strong>Zero self-service for corporate clients:</strong> every "where is my shipment?" call interrupted a supervisor's work.</li>
                        <li><strong>End-of-day closing took 2+ hours:</strong> supervisors reconciled pickups vs manifests vs delivered vs COD collected, manually.</li>
                    </ul>
                </div>

                <div class="cs-block">
                    <h2>The solution: model the entire pickup-to-POD workflow, then ship it</h2>
                    <p>The fundamental mistake small-courier software makes is treating this as "a form to enter an AWB." It isn't. It's a chain of physical hand-offs that must be reflected faithfully in software, with each hand-off recorded by the person actually doing it, on the device they have in their hand.</p>
                    <p>We spent three weeks embedded with the ops team (head office, one city hub, one line-haul route, one delivery beat) before writing a line of code. What came out was a five-stage workflow that the CMS models end to end:</p>

                    <div class="workflow-diagram">
                        <div class="workflow-steps">
                            <div class="workflow-step"><span class="ws-num">1</span><span class="ws-title">Booking &amp; Pickup</span><span class="ws-desc">Corporate portal or field booking app</span></div>
                            <div class="workflow-step"><span class="ws-num">2</span><span class="ws-title">Hub In-Scan</span><span class="ws-desc">Barcode scan + weigh + dimensional check</span></div>
                            <div class="workflow-step"><span class="ws-num">3</span><span class="ws-title">Manifest &amp; Line-Haul</span><span class="ws-desc">Hub-to-hub bag + vehicle + seal</span></div>
                            <div class="workflow-step"><span class="ws-num">4</span><span class="ws-title">Delivery &amp; POD</span><span class="ws-desc">Beat planning + proof of delivery</span></div>
                            <div class="workflow-step"><span class="ws-num">5</span><span class="ws-title">Billing &amp; MIS</span><span class="ws-desc">Auto-reconciled; no manual sheets</span></div>
