                    <h3>Modules we built</h3>
                    <ul>
                        <li><strong>Booking:</strong> corporate client portal (web) + field booking on Android + walk-in counter screen. Every booking generates a printable AWB and feeds the pickup queue in real time.</li>
                        <li><strong>Pickup management:</strong> beat-wise pickup lists, pickup boy Android app with barcode scan + photo capture + signature, auto cross-check against booked volume.</li>
                        <li><strong>Hub operations:</strong> in-scan, sort-by-destination, out-scan, hand-over to line-haul. Barcode-first — the hub floor never has to type an AWB.</li>
                        <li><strong>Manifest &amp; line-haul:</strong> digital manifest with piece-count + weight checks, vehicle + driver + seal record, GPS trace of line-haul runs.</li>
                        <li><strong>Delivery:</strong> beat planning, delivery boy app with POD capture (signature, photo, GPS stamp), re-attempt flow, COD collection.</li>
                        <li><strong>Billing:</strong> auto-calculated invoices per contract, weight dispute workflow, COD remittance, monthly statements — zero manual Excel.</li>
                        <li><strong>Tracking &amp; MIS:</strong> corporate tracking portal (white-labelled), public tracking, head-office dashboards for SLA breach, pendency, revenue, hub utilisation.</li>
                    </ul>
                </div>

                <div class="cs-block">
                    <h2>Tech stack &amp; why</h2>
                    <p>This is a high-volume, low-margin, high-reliability system. It needed to be cheap to operate, easy to host on Indian infrastructure, and simple enough that a future in-house team could maintain it. That pointed firmly at a battle-tested LAMP-style stack over anything trendy:</p>
                    <div style="margin-bottom: 16px;">
                        <span class="tech-chip">PHP 8 (Laravel 10)</span>
                        <span class="tech-chip">MySQL 8</span>
                        <span class="tech-chip">Blade + Alpine.js</span>
                        <span class="tech-chip">Livewire</span>
                        <span class="tech-chip">Redis</span>
                        <span class="tech-chip">Android (Kotlin)</span>
                        <span class="tech-chip">Zebra/Honeywell scanner SDKs</span>
                        <span class="tech-chip">MSG91 SMS</span>
                        <span class="tech-chip">WhatsApp Business API</span>
                        <span class="tech-chip">AWS EC2 + RDS</span>
                        <span class="tech-chip">CloudWatch</span>
                        <span class="tech-chip">GitHub Actions</span>
                    </div>
                    <p><strong>Laravel for the web CMS:</strong> fast to build, huge ecosystem, and — critical for this client — every second PHP developer in India already knows it. When the client eventually hires an in-house dev, onboarding will take a week, not a quarter.</p>
                    <p><strong>Kotlin Android for field apps:</strong> the devices in the field are rugged Android scanners from Zebra and Honeywell. Native Kotlin means first-class support for the scanner SDKs, which are absolutely not a Flutter-friendly world. iOS was explicitly not required.</p>
                    <p><strong>MySQL over anything exotic:</strong> 5M+ shipment rows, 20M+ scan events, heavy read load on tracking queries. Well-indexed MySQL handles this effortlessly and is operationally boring — which for a logistics back-office is a compliment.</p>
                    <p><strong>Redis for hot tracking:</strong> the corporate tracking portal sees the same AWB looked up dozens of times. Redis caches the "latest known status" and cuts MySQL load to a trickle.</p>
                </div>

                <div class="cs-block">
                    <h2>The hardest non-technical problem we solved</h2>
                    <p>Rollout. The software was the easy part. Moving 35 people off a 20-year-old Excel workflow onto a new platform — without grinding a working business to a halt — was the real work.</p>
                    <p>We learned this the hard way on the first hub. A big-bang switchover caused two days of chaos: new UI, unfamiliar scanner flow, a couple of genuine bugs, and a supervisor who'd been doing manifests on Excel since 2005 deciding on day two that "the old way was better." Volume that hub handled halved for 48 hours.</p>
                    <p>For the remaining 13 hubs we switched to a dual-running rollout:</p>
                    <ul>
                        <li><strong>Week 1:</strong> new system running in shadow mode — real data captured, but Excel is still the source of truth.</li>
                        <li><strong>Week 2:</strong> new system becomes the source of truth for bookings and pickups; hub/manifest still on Excel.</li>
                        <li><strong>Week 3:</strong> hub/manifest flips to CMS; Excel becomes read-only reference.</li>
                        <li><strong>Week 4:</strong> Excel sunset.</li>
                    </ul>
                    <p>A dedicated field trainer (from our side) was physically present for the first week in each hub. The next 13 rollouts had zero volume disruption.</p>
                </div>

