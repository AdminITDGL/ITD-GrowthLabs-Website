                <div class="cs-block">
                    <h2>The client &amp; the problem</h2>
                    <p>The developer was running their pipeline on a mix of MagicBricks back-end, an ageing Salesforce instance two SDRs barely opened, and Excel. By the time a lead reached a sales person it was 18-36 hours cold. The pitch we made to them: speed-to-first-touch is the single biggest lever in real-estate, and WhatsApp + automation is how you get there.</p>
                    <ul>
                        <li><strong>Slow first response:</strong> Average time-to-first-call was 14 hours; competitors were under one.</li>
                        <li><strong>Channel-partner blackbox:</strong> 200+ brokers brought leads but had no app or visibility — they were leaving for competitors with better tooling.</li>
                        <li><strong>Sales-app dysfunction:</strong> Sales people kept notes on phones and lost half of them.</li>
                        <li><strong>Re-engagement gap:</strong> After a missed call, a lead was effectively dead — there was no automated nurture.</li>
                        <li><strong>WhatsApp risk:</strong> The team WhatsApp-pasted brochures from personal numbers; one number got banned, blowing up that quarter's pipeline.</li>
                    </ul>
                </div>

                <div class="cs-block">
                    <h2>The solution</h2>
                    <p>Three apps + a WhatsApp automation engine. The non-obvious decision was building the channel-partner app first; brokers contributed 60% of leads and our competitors were not investing in them.</p>
                    <h3>1. CRM web app (React)</h3>
                    <p>Lead inbox with auto-assignment, multi-source ingestion (MagicBricks, 99acres, FB lead-form, web forms), pipeline stages, site-visit booking, document drawer, communication log (calls, WhatsApp, email), property-specific dashboards.</p>
                    <h3>2. Sales-team mobile app (React Native)</h3>
                    <p>Day-view of calls + visits, one-tap call (with auto-log), WhatsApp template send, geo-tagged site-visit attendance, voice-note capture, deal-stage update, commission view.</p>
                    <h3>3. Channel-partner app (React Native)</h3>
                    <p>Brokers add their own clients, get real-time updates on lead status, see their commission earned, and book site visits. This single feature made us non-trivial to switch away from.</p>
                    <h3>4. WhatsApp automation engine</h3>
                    <p>WhatsApp Cloud API integration. Templates for: instant lead acknowledgement, brochure send, virtual-tour link, site-visit reminder, post-visit thank-you, post-visit drip (5-message sequence over 14 days). Inbound replies route to sales app.</p>
                    <h3>5. Backend (Laravel + Postgres)</h3>
                    <p>Laravel 10, Postgres for relational data, Redis for queueing, BullMQ-style worker for WhatsApp throttling and template-rate-limit handling, S3 for documents, Twilio fallback for SMS.</p>
                </div>

                <div class="cs-block">
                    <h2>Tech stack &amp; why</h2>
                    <p>We picked the stack for fit, performance under realistic load, and operational simplicity. Here is the breakdown:</p>
                    <div style="margin-bottom: 16px;">
                        <span class="tech-chip">Laravel 10</span>
                        <span class="tech-chip">PHP 8.2</span>
                        <span class="tech-chip">PostgreSQL</span>
                        <span class="tech-chip">Redis</span>
                        <span class="tech-chip">React 18</span>
                        <span class="tech-chip">React Native</span>
                        <span class="tech-chip">WhatsApp Cloud API</span>
                        <span class="tech-chip">Twilio</span>
                        <span class="tech-chip">Razorpay</span>
                        <span class="tech-chip">AWS S3</span>
                        <span class="tech-chip">GitHub Actions</span>
                        <span class="tech-chip">Sentry</span>
                        <span class="tech-chip">Datadog</span>
                    </div>
                    <p><strong>WhatsApp Cloud API, not vendor wrapper:</strong> we integrated directly to Meta's Cloud API instead of using a Wati / AiSensy reseller. Saves Rs 25-40 per 1,000 messages at scale and eliminates the third-party-outage risk that hit us once on a competing build.</p>
                    <p><strong>Laravel for the CRM API:</strong> the team's strongest stack, ecosystem (Eloquent, Sanctum, Horizon) covered 80% of what we needed. Performance was a non-issue at 28,000 leads — would re-evaluate at 1M.</p>
                    <p><strong>React Native for two apps:</strong> sales and channel-partner apps share 60% of code. Half the build budget of two native iOS + Android codebases.</p>
                </div>

                <div class="cs-block">
                    <h2>The hardest technical problem we solved</h2>
                    <p><strong>WhatsApp template approvals at scale.</strong></p>
<p>WhatsApp's template-approval policy is undocumented in any useful sense and changes month-to-month. A real-estate developer needs ~25 distinct templates: lead ack, brochure, RERA disclosure, virtual tour, visit reminder, visit confirmation, post-visit thank-you, drip 1-5, etc. Submit them naively and 40% get rejected.</p><p>We built a template-versioning system inside the CRM. Each template has versions; rejections store Meta's reason code; a team-mate can iterate without losing history. We also encoded the policy patterns we'd learned (no commercial language in &quot;authentication&quot; templates, no embedded URLs in &quot;marketing&quot; templates without variable mapping, etc.).</p><p>Approval rate went from ~60% (industry default) to 92%. Time-to-launch a new template went from 3 days to 4 hours.</p>
                </div>

                <div class="cs-block">
                    <h2>Go-to-market &amp; ramp</h2>
                    <p>We launched on a single property — 1,200 existing leads, 12 sales people, 30 channel partners. Two weeks of intensive ops involvement. Then second property, then third. By property five the on-boarding was a 3-day playbook.</p><p>Channel-partner adoption was the leading indicator. When we hit 70% of partners using the app weekly, lead quality jumped because partners shared their best clients, not their leftovers.</p>
