                                        <h2>Frequently Asked Questions</h2>
                                        <h3>How much does it cost to build a Practo-style doctor appointment app?</h3>
                                        <p>MVP appointment-only: &#8377;8L–&#8377;12L over 6–8 weeks. Full booking + telemed + e-prescription: &#8377;15L–&#8377;22L over 10–14 weeks. Enterprise multi-clinic with EHR integration: &#8377;25L+ over 14–20 weeks.</p>
                                        <h3>How do I onboard doctors to a new appointment app?</h3>
                                        <p>Doctor acquisition takes 8–12 weeks of dedicated BD. Make onboarding self-serve (NMC verification API), keep the doctor app simple, send WhatsApp notifications, and pay out earnings same-week. The doctor app UX matters more than the patient side because doctors are the supply-side bottleneck.</p>
                                        <h3>How do I reduce no-show rates?</h3>
                                        <p>Combine WhatsApp reminders (24h + 4h + 30min before), a small refundable deposit (&#8377;100–&#8377;200), one-tap reschedule, and walk-up slots for buffer. Together these can move 25–40% no-show rate to 10–15%.</p>
                                        <h3>What's the revenue model for a doctor booking app?</h3>
                                        <p>Three common models: per-consultation transaction fee (10–20%), premium doctor listings/promotion, and subscription fees for clinics with dashboards/EHR. Most successful Indian doctor apps use 2–3 of these in combination. Free booking with no monetisation rarely sustains.</p>
                                        <h3>Should I build it custom or use a SaaS?</h3>
                                        <p>SaaS (Practo Profile, Lybrate, MediBuddy partner) suits individual clinics. Build custom if you're running a network, marketplace, or specialty-focused app where workflows differ from generic SaaS.</p>
