                                    <p>
                                        - <strong>Profile information</strong>:They collect information that is provided by the user when they setup the an account, such as your date of birth, username, email address and/or telephone number, and password.
                                        <br>
                                        - <strong>User Input</strong>:When the user use their services, they may collect their text or audio input, prompt, uploaded files, feedback, chat history, or other content provided by the user to their model and services.
                                        <br>
                                        - <strong>Information when user contact the company
                                        </strong>:When the user contact them, they collect the information that is sent by the user, such as proof of identity or age, feedback or inquiries about the use of their service about the possible violations of their Terms of Service or other policies.
                                        <br>
                                    </p>

                                    <h3>Automatically Collected Information</h3>

                                    <p>
                                        - <strong>Technical Information</strong>: They collect certain device and network connection information when user access the service. This information includes, device model, operating system, keystroke patterns or rhythms, IP address and system language. They also collect service-related, diagnostic, and performance information, including crash reports and performance logs.
