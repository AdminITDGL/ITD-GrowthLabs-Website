                                    <h3>DeepSeek
                                    </h3>

                                    <p>DeepSeek is the name of the LLM model that is currently open-source and is developed by the Chinese Artificial Intelligence company named Hangzhou DeepSeek Artificial Intelligence Co., Ltd. It was established in the year 2023, and its co-founder is Liang Wenfeng who is serving as the CEO of the company. The company’s headquarter is in Hangzhou, Zhejiang, China.</p>

                                    <p>DeepSeek had launched two of its best LLM model namely, V2 and V3 which were near to the model chatGPT-4o. However, in Jan 2025 they launched their new model, R1, which is giving a tough competition to chatGPT-o1 model as it is better than chatGPT-o1 or similar to chatGPT-o1.</p>

                                    <h3>Comparison</h3>
                                    <p>Privacy Policy</p>
                                    <p>I will just point out the key points from the whole privacy policy mentioned by the companies. In this section I will elaborate what data are they taking from users.</p>

                                    <p>
                                        - <strong>Account Information</strong>:They will be collecting the information associated with the account which which the user have logged in, these information are name, contact information, account credentials, date of birth, payment information, and transaction history.
                                        <br>
                                        - <strong>User Content</strong>:The data which user provide in the input to their services, which includes, prompts, uploaded files/images and audio, depending on the feature used by the user.
                                        <br>
                                        - <strong>Communication Information
                                        </strong>: If user is communicating with the company via email or social media sites, then they may collect Personal Data like user’s name, contact information, and the contents of the messages that user send
                                        <br>
                                    </p>

                                    <h3> Personal Data which they get when user uses their service:</h3>

                                    <p>
                                        - <strong>Log Data</strong>:They collect information that user’s browser or device automatically sends when user uses their service. This log data includes user’s IP address, browser type and settings, the data and time of user’s request, and how user interact with their service.
                                        <br>
                                        - <strong>Usage Data</strong>:They collect information about their use of service, such as the types of content that user view or engage with, the feature that user use and the actions user take, as well as user’s time zone, country, the dates and time of access, user agent and version, type of computer or mobile device, and user’s computer connection.
                                        <br>
                                        - <strong>Location Information
                                        </strong>:They may determine the general area from which the user’s device accesses their service based on the information like it is IP address for security reasons and to make the product experience better.
                                        <br>
                                    </p>

                                    <p>They use the personal data provided for many different purposes, such as to provide analyze and maintain their service, to improve and develop their services, to communicate with the users, to prevent fraud, illegal activity, or misuses of their services, and to protect the security of their system and to comply with legal obligations and to protect the rights, privacy, safety or property of their users, OpenAI or third parties.</p>

