                                    <p>
                                        - <strong>Log Data</strong>:They collect information that user’s browser or device automatically sends when user uses their service. This log data includes user’s IP address, browser type and settings, the data and time of user’s request, and how user interact with their service.
                                        <br>
                                        - <strong>Usage Data</strong>:They collect information about their use of service, such as the types of content that user view or engage with, the feature that user use and the actions user take, as well as user’s time zone, country, the dates and time of access, user agent and version, type of computer or mobile device, and user’s computer connection.
                                        <br>
