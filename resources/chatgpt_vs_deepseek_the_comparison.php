                                        - <strong>Profile information</strong>:They collect information that is provided by the user when they setup the an account, such as your date of birth, username, email address and/or telephone number, and password.
                                        <br>
                                        - <strong>User Input</strong>:When the user use their services, they may collect their text or audio input, prompt, uploaded files, feedback, chat history, or other content provided by the user to their model and services.
                                        <br>
                                        - <strong>Information when user contact the company
